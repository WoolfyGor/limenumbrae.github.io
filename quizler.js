class QuizQuestion{
    constructor(question,answers){
        this.question = question;
        this.answers = answers
    }

    toString(){
        return `${this.question} | ${this.answers}`
    }

    getQuestion() {
        return this.question
    }

    getAnswers(){
        return this.answers
    }
}

